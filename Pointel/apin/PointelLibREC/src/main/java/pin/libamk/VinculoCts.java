package pin.libamk;

public class VinculoCts {

    public String nomeEnu;
    public Object valorEnu;
    public String nomePai;
    public String nomeCts;

    public VinculoCts(String nomeEnu, Object valorEnu, String nomePai, String nomeCts) {
        this.nomeEnu = nomeEnu;
        this.valorEnu = valorEnu;
        this.nomePai = nomePai;
        this.nomeCts = nomeCts;
    }

}
