package pin;

public class MesaAtalho {
    
    public String raiz;
    public String nome;
    public Integer posX;
    public Integer posY;

    public MesaAtalho(String raiz, String nome, Integer posX, Integer posY) {
        this.raiz = raiz;
        this.nome = nome;
        this.posX = posX;
        this.posY = posY;
    }
    
}
