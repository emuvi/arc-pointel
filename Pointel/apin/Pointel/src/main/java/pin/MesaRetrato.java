package pin;

public class MesaRetrato {
    public String endereco;
    public Integer posX;
    public Integer posY;
    public Integer largura;
    public Integer altura;

    public MesaRetrato(String endereco, Integer posX, Integer posY, Integer largura, Integer altura) {
        this.endereco = endereco;
        this.posX = posX;
        this.posY = posY;
        this.largura = largura;
        this.altura = altura;
    }
}
